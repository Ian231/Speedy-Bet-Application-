    private void FootballB25ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FootballB25ActionPerformed

        if (ResultAlgo.GainAndLoss > 0) {
            JOptionPane.showMessageDialog(Bank, "Hello " + Customer.C_FirstName + "Congratulations Your total amount of winnings comes up to "
                    + ResultAlgo.GainAndLoss + "\n");

        } else if (ResultAlgo.GainAndLoss > 0) {
            JOptionPane.showMessageDialog(Bank, "Hello " + Customer.C_FirstName + "your bet has been verified as a loser  "
                    + (ResultAlgo.GainAndLoss * -1) + "\nthe losses will be deducted from your bank account.\n");
        } else {
            JOptionPane.showMessageDialog(Bank, "Hello " + Customer.C_FirstName + "\n unfortunately your bet has not been processed ");
        }

        String arrtypes[] = new String[TableNotification.getRowCount()];
        String namee[] = new String[TableNotification.getRowCount()];
        DefaultTableModel dtm = (DefaultTableModel) TableNotification.getModel();

        for (int i = 0; i < TableNotification.getRowCount(); i++) {
            arrtypes[i] = dtm.getValueAt(i, 1) + "";
            namee[i] = dtm.getValueAt(i, 0) + "";
            System.out.println(arrtypes[i] + "      Names" + namee[i]);
        }

        try {
            new Payment().deleteMyPayments(arrtypes, namee);
            new Bank().UpdateBankAccountMoneyData(ResultAlgo.GainAndLoss);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        }

        JOptionPane.showMessageDialog(Bank, "Hello " + Customer.C_FirstName + "\nYour winnings have now been allocated to your account");
        // carde layout to show wellcome  panale 
        ccc = (CardLayout) (F.jPanel1.getLayout());
        ccc.show(F.jPanel1, "welcome");

    }//GEN-LAST:event_FootballB25ActionPerformed

